package org.mullercamille.edtdroid.model;

public class Group {
	private String name;
	private String value;

	public Group(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return this.name;
	}

	public String getValue() {
		return this.value;
	}
}
	